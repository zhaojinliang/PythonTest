function [h, T1]=alpha_h(alpha_1, v, k)
% param: alpha_1 第一段锚链与水平面之间的角度
% param: v 风速
% return: h 浸水深度
%         T1 锚在水中受到的拉力
%==========================================================================
%40738.875
h = (2.5*tan(alpha_1)*v^2+24138.875-7.35*k) / (32185+1.25*tan(alpha_1)*v^2);
T1 = ((2.5-1.25*h)*v^2) / cos(alpha_1);
end